package frc.robot.commands;

import edu.wpi.first.wpilibj2.command.CommandBase;
import frc.robot.Constants;
import frc.robot.subsystems.Chassis;

public class Drive_By_Encoder extends CommandBase {
  /**
   * Creates a new Drive_By_Encoder.
   */
  Chassis chassis;

  public Drive_By_Encoder(Chassis chassis) {
    this.chassis = chassis;
    addRequirements(chassis);
    // Use addRequirements() here to declare subsystem dependencies.
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {
  }

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    if(chassis.getEncoder(2)<Constants.pulses_in_meter){
      chassis.Set_velocity(0.2);
    }
    System.out.println("second:");
    System.out.println(chassis.getEncoder(2));

  }

  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {
  }

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
